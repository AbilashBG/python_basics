
# String Manipulation

'''

 S    a   m   p   l   e
 0    1   2   3   4   5
-6   -5  -4  -3  -2  -1

'''

s="Sample"

# note => [index : position]

print(s) 
# Sample

print(s[0:2])
# Sa

print(s[:5])
# Sampl

print(s[1:])
# ample

print(s[-1])
# e

print(s[-2:-1])
# l

print(s[:-1])
# Sampl

print(s[::-1])
# elpmaS



